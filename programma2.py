def addition(number1, number2):
		eind = number1+number2
		return str(number1) +" + "+str(number2)+" = "+str(eind)
print(addition(10,12))

def subtraction(number1, number2):
        eind = number1 - number2
        return str(number1) +" - "+ str(number2)+ " = "+str(eind)
print(subtraction(58,34))

def multiplication(number1, number2):
        eind = number1 * number2
        return str(number1) +" x "+ str(number2)+ " = "+str(eind)
print(multiplication(6,7))

def division(number1, number2):
        eind = number1 / number2
        return str(number1) +" : "+ str(number2)+ " = "+str(eind)
print(division(144,12))

def increment(number1, number2):
        eind = number1 + number2
        return str(number1) +" + "+ str(number2)+ " = "+str(eind)
print(increment(12,1))

def decrement(number1, number2):
        eind = number1 - number2
        return str(number1) +" - "+ str(number2)+ " = "+str(eind)
print(decrement(34,1))


    